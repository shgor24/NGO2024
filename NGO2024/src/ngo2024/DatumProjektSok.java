    private void bSokActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSokActionPerformed
        String startdatum = ftfStartdatum.getText();
        String slutdatum = ftfSlutdatum.getText();

        //Denna if-sats kontrollerar att både start- och slutdatumen är skrivna i rätt format, samt har giltiga värden för dag och månad
        if (!Validering.datumValidering(startdatum) || !Validering.datumValidering(slutdatum)) {
            JOptionPane.showMessageDialog(this, "Felaktigt Start- eller Slutdatum \nVänligen ange datum i formatet: YYYY-MM-DD");
        } else if (!Validering.manadsValidering(startdatum) || !Validering.manadsValidering(slutdatum)) {
            JOptionPane.showMessageDialog(this, "Felaktigt värde på månad i Start- eller Slutdatumet \nVänligen ange en giltig månad mellan 01 - 12");
        } else if (!Validering.dagValidering(startdatum) || !Validering.dagValidering(slutdatum)) {
            JOptionPane.showMessageDialog(this, "Felaktigt värde på dag i Start- eller Slutdatumet \nVänligen ange en giltig dag mellan 01 - 31");
        } else if (!Validering.giltigDatumSpan(startdatum, slutdatum)) {
            JOptionPane.showMessageDialog(this, "Felaktigt datumintervall \nVänligen se till att startdatumet är innan slutdatumet");
        } else{
            SoktDatumProjektMeny datumProjektMeny = new SoktDatumProjektMeny(idb, aid);
            datumProjektMeny.hamtaRelevantaProjekt(startdatum, slutdatum);
            datumProjektMeny.setVisible(true);
        } 
        
    }//GEN-LAST:event_bSokActionPerformed

